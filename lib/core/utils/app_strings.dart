  static String localizeBackendMessage(String message, Locale locale) {
    final normalized = message.trim();
    if (normalized.isEmpty) {
      return locale.languageCode == 'ar' ? 'تمت العملية بنجاح' : 'Operation completed successfully';
    }

    final lower = normalized.toLowerCase();
    final sanitized = lower
        .replaceAll(RegExp(r'[.!؟!]+$'), '')
        .replaceAll(RegExp(r'\s+'), ' ')
        .trim();

    if (locale.languageCode == 'ar') {
      if (_localizedMessages.containsKey(lower)) {
        return _localizedMessages[lower]!;
      }
      if (_localizedMessages.containsKey(sanitized)) {
        return _localizedMessages[sanitized]!;
      }
    }
    // For English or not found, return original normalized
    return normalized;
  }

